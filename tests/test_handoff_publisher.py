class TestImportSafety(unittest.TestCase):
    """Group 9: handoff_publisher.py does not import forbidden modules."""

    def _publisher_imports(self) -> set[str]:
        src = open(os.path.join(_ROOT, "handoff_publisher.py")).read()
        tree = ast.parse(src)
        names: set[str] = set()
        for node in ast.walk(tree):
            if isinstance(node, ast.Import):
                for alias in node.names:
                    names.add(alias.name.split(".")[0])
            elif isinstance(node, ast.ImportFrom):
                if node.module:
                    names.add(node.module.split(".")[0])
        return names

