    def test_invalid_output_does_not_overwrite_valid_file(self):
        """Fail-closed cycle: universe and manifest not overwritten."""
        sys.path.insert(0, _ROOT)
        import handoff_publisher as hp
        with tempfile.TemporaryDirectory() as tmpdir:
            # Write a "valid" sentinel universe
            sentinel_universe = os.path.join(tmpdir, "active_opportunity_universe.json")
            sentinel_manifest = os.path.join(tmpdir, "current_manifest.json")
            original_content = {"sentinel": True}
            with open(sentinel_universe, "w") as f:
                json.dump(original_content, f)
            with open(sentinel_manifest, "w") as f:
                json.dump(original_content, f)

            # Run publisher with missing source — should fail closed
            missing = os.path.join(tmpdir, "nonexistent.json")
            with (
                patch.object(hp, "_SHADOW_UNIVERSE_PATH", missing),
                patch.object(hp, "_OUTPUT_UNIVERSE", sentinel_universe),
                patch.object(hp, "_OUTPUT_MANIFEST", sentinel_manifest),
                patch.object(hp, "_OUTPUT_REPORT", os.path.join(tmpdir, "report.json")),
                patch.object(hp, "_OUTPUT_HEARTBEAT", os.path.join(tmpdir, "hb.json")),
            ):
                hp.run_publisher()

            # Sentinel files must be unchanged
            with open(sentinel_universe) as f:
                result = json.load(f)
            self.assertTrue(result.get("sentinel"))

            with open(sentinel_manifest) as f:
                result = json.load(f)
            self.assertTrue(result.get("sentinel"))

